#addition
def add(a, b):
    result = a + b
    return result

#subtraction
def subtract(a, b):
    result = a - b
    return result

#multiplication
def multiply(a, b):
    result = a * b
    return result

#division
def divide(a, b):
    result = a / b
    return result


